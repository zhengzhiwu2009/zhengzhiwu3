package com.whaty.platform.entity.bean;

public class StudentElectiveVO {
	private String courseId;
	private String courseCode;
	private String courseName;
	private String courseTime;
	private String courseType;
	private String learningStatus;
	private String opencourseId;
	private String studentName;
	
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getCourseId() {
		return courseId;
	}
	public void setCourseId(String courseId) {
		this.courseId = courseId;
	}
	public String getCourseCode() {
		return courseCode;
	}
	public void setCourseCode(String courseCode) {
		this.courseCode = courseCode;
	}
	public String getCourseName() {
		return courseName;
	}
	public void setCourseName(String courseName) {
		this.courseName = courseName;
	}
	public String getCourseTime() {
		return courseTime;
	}
	public void setCourseTime(String courseTime) {
		this.courseTime = courseTime;
	}
	public String getCourseType() {
		return courseType;
	}
	public void setCourseType(String courseType) {
		this.courseType = courseType;
	}
	public String getLearningStatus() {
		return learningStatus;
	}
	public void setLearningStatus(String learningStatus) {
		this.learningStatus = learningStatus;
	}
	public String getOpencourseId() {
		return opencourseId;
	}
	public void setOpencourseId(String opencourseId) {
		this.opencourseId = opencourseId;
	}
	
	
}
