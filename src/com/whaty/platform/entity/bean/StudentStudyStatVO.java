package com.whaty.platform.entity.bean;

public class StudentStudyStatVO {
	private String id;
	private String name;
	private String peName;
	private String cardNo;
	private String department;
	private String ffClass;
	private String hdClass;
	private String totalClass;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPeName() {
		return peName;
	}
	public void setPeName(String peName) {
		this.peName = peName;
	}
	public String getCardNo() {
		return cardNo;
	}
	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getFfClass() {
		return ffClass;
	}
	public void setFfClass(String ffClass) {
		this.ffClass = ffClass;
	}
	public String getHdClass() {
		return hdClass;
	}
	public void setHdClass(String hdClass) {
		this.hdClass = hdClass;
	}
	public String getTotalClass() {
		return totalClass;
	}
	public void setTotalClass(String totalClass) {
		this.totalClass = totalClass;
	}
}
